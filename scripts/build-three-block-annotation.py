def patch_order_pick(text: str) -> str:
    pattern = (
        r"('card-order-pick': \{[\s\S]*?"
        r"'3\. 预览方案 \| 须至少勾选 1 个产品；进入方案预览卡',)"
    )
    extra = "\n".join(ORDER_PICK_EXTRA)
    new, n = re.subn(pattern, lambda m: m.group(1) + "\n" + extra, text, count=1)
    if n != 1:
        raise SystemExit(f"card-order-pick patch failed, n={n}")
    return new


def build(copy_submit_note: str) -> str:
    base = V12.read_text(encoding="utf-8")
    base = patch_order_pick(base)
    base = base.rstrip()
    if base.endswith("};"):
        base = base[:-2].rstrip()
    if not base.endswith("},"):
        if base.endswith("}"):
            base = base + ","
    body = MODULE_05_06.strip()
