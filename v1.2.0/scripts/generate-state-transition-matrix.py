        "# 意图识别说明 · 方案速配 / 产品报价 / 确认下单（v1.2.0）",
        "",
        "> 依据 [`功能描述-方案报价下单-v1.2.0.md`](./功能描述-方案报价下单-v1.2.0.md) 穷举用户在**各卡片**上的输入及系统跳转结果。",
