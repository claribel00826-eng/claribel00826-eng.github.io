def normalize_complete_mode(row: tuple[str, ...]) -> tuple[str, ...]:
    """完整需求 = 四功能槽位齐全且直达最终页；其余降级并修正订单落点。"""
    cur, fn, intent, mode, utter, tgt, slots = row

    # 订单完整需求：最终页必须是下单确认卡
    if tgt == "订单成功卡" and mode == "完整需求":
        return (cur, fn, intent, mode, utter, "下单确认卡", slots)
    if mode != "完整需求":
        return row

    if tgt in FINAL_TARGETS:
        return row

    # 非最终页：不应标为完整需求
    if tgt == cur or "数量" in utter or "规格" in utter or utter.startswith("选品"):
        mode = "表单填充"
    elif utter in ("第1条", "第2条", "第1个") or utter.startswith("点选") or "第" in utter and "条" in utter:
        mode = "模拟点击"
    elif cur in INIT_PAGE_TARGETS or tgt in INIT_PAGE_TARGETS:
        mode = "部分需求"
    else:
        mode = "部分需求"
    return (cur, fn, intent, mode, utter, tgt, slots)


