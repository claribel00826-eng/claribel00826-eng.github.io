def write_xlsx(path: Path) -> None:
    if Workbook is None:
        print("openpyxl not installed, skip xlsx")
        return
    normalized = normalize_rows(ROWS)
    export_rows = dedupe_output_rows(normalized)
    wb = Workbook()
    ws = wb.active
    ws.title = "意图识别说明"
    hf = PatternFill("solid", fgColor="1F4E79")
    hfont = Font(bold=True, color="FFFFFF", size=11)
    for col, h in enumerate(OUTPUT_HEADERS, 1):
        c = ws.cell(row=1, column=col, value=h)
        c.fill, c.font = hf, hfont
        c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
    xlsx_rows = sort_all_export_rows(export_rows)
    for r, row in enumerate(xlsx_rows, 2):
        for c, val in enumerate(to_export_row(row), 1):
            cell = ws.cell(row=r, column=c, value=val)
            cell.alignment = Alignment(vertical="top", wrap_text=True)
    widths = [40, 48, 12, 32, 24]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w
    ws.freeze_panes = "A2"
    last_col = get_column_letter(len(OUTPUT_HEADERS))
    ws.auto_filter.ref = f"A1:{last_col}{len(export_rows) + 1}"
    meta = wb.create_sheet("说明")
    meta["A1"] = "版本"
    meta["B1"] = "v1.2.0"
    meta["A3"] = "源文档"
    meta["B3"] = "功能描述-方案报价下单-v1.2.0.md"
    meta["A5"] = "行数"
    meta["B5"] = len(export_rows)
    meta["A7"] = "列"
    meta["B7"] = "上下文、用户输入、交互模式、解析槽位、目标卡片"
    meta["A9"] = "排序"
    meta["B9"] = "全局能力→初始化页面→待跟进→方案速配→方案报价→确认订单；章内按目标卡片主流程顺序分组"
    path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(path)


def main() -> None:
    out_md = Path(sys.argv[1]) if len(sys.argv) > 1 else MD_OUT
    out_xlsx = out_md.with_suffix(".xlsx") if out_md.suffix == ".md" else XLSX_OUT
    write_markdown(out_md)
    write_xlsx(out_xlsx if out_xlsx != out_md else XLSX_OUT)
    normalized = normalize_rows(ROWS)
    export_rows = dedupe_output_rows(normalized)
    conflicts = find_utterance_slot_conflicts(export_rows)
    if conflicts:
        print("WARN: utterance/slot conflicts (same section+input+mode, different parsed slots):")
        for c in conflicts:
            print(" ", c)
    n = len(export_rows)
    print(f"Wrote {n} rows (deduped from {len(ROWS)}) -> {out_md}")
    if Workbook:
        print(f"Wrote xlsx -> {out_xlsx if out_xlsx != out_md else XLSX_OUT}")


if __name__ == "__main__":
    main()
