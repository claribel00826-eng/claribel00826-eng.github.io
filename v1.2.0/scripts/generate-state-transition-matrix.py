def _mode_key(row: tuple[str, ...]) -> tuple[int, str, str]:
    return (MODE_ORDER.get(row[3], 9), row[2], row[4])


def sort_rows(rows: list[tuple[str, ...]]) -> list[tuple[str, ...]]:
    def key(row: tuple[str, ...]) -> tuple[int, int, tuple[int, str, str]]:
        fn, cur = row[1], row[0]
        fi = FUNC_ORDER.index(fn) if fn in FUNC_ORDER else 99
        cards = CARD_ORDER.get(fn, [])
        ci = cards.index(cur) if cur in cards else 999
        return (fi, ci, _mode_key(row))

    return sorted(rows, key=key)


def group_rows(rows: list[tuple[str, ...]]) -> list[tuple[str, str | None, list[tuple[str, ...]]]]:
    """返回 [(功能名, 子步骤标签|None, 行列表), ...]"""
    sorted_r = sort_rows(rows)
    out: list[tuple[str, str | None, list[tuple[str, ...]]]] = []
    for fn in FUNC_ORDER:
        fn_rows = [r for r in sorted_r if r[1] == fn]
        if not fn_rows:
            continue
        cards = CARD_ORDER.get(fn, [])
        seen: set[str] = set()
        for card in cards:
            card_rows = [r for r in fn_rows if r[0] == card]
            if not card_rows:
                continue
            seen.add(card)
            label = STEP_LABEL.get(fn, {}).get(card)
            out.append((fn, label, sorted(card_rows, key=_mode_key)))
        for card in dict.fromkeys(r[0] for r in fn_rows):
            if card not in seen:
                extra = [r for r in fn_rows if r[0] == card]
                out.append((fn, None, sorted(extra, key=_mode_key)))
    return out


def normalize_user_utterance(utter: str) -> str:
    """用户输入列：仅保留用户说出口的话（或说出的按钮/菜单文案），去掉说明性括号与「点选」类描述。"""
    u = (utter or "").strip()
    if not u:
        return u

    # 整句仅为备注、无口语 → 导出时留空（由解析槽位说明）
    if re.fullmatch(r"（[^）]+）", u):
        return ""

    # 去掉前缀/内联括号说明，保留其后口语
    u = re.sub(r"（[^）]*）", "", u).strip()

    # 「在某某卡中说」→ 只保留引号内话术
    m = re.search(r"中说[「\"](.+?)[」\"]", u)
    if m:
        return m.group(1).strip()

    # 点选 / 勾选 → 用户直接说客户名、品名或按钮文案
    m = re.fullmatch(r"点选[「\"](.+?)[」\"]", u)
    if m:
        return m.group(1).strip()
    u = (
        u.replace("点选第1行方案", "第1条")
        .replace("点选第1行", "第1条")
        .replace("点选方案第1行", "第1条")
        .replace("点选客户行", "第1条")
    )

    m = re.fullmatch(r"勾选[「\"](.+?)[」\"]", u)
    if m:
        return f"勾选{m.group(1)}".strip()

    u = u.replace("选「", "").replace("」", "")
    u = re.sub(r"输入筛选词\s*->\s*", "", u)
    u = re.sub(r"勾选产品\s*->\s*", "", u)
    u = re.sub(r"点选新客户", "深圳创源科技有限公司", u)

    u = re.sub(r"\s*->\s*", " -> ", u)
    u = re.sub(r"\s+", " ", u).strip()
    return u


