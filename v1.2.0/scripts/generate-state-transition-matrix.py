def _is_context_segment(seg: str) -> bool:
    s = seg.strip()
    if not s or s.startswith("缺："):
        return False
    if "（来自上文）" in s:
        return True
    return bool(_CONTEXT_SEG_RE.search(s))


def split_parsed_and_context(cur: str, raw_slots: str) -> tuple[str, str]:
    """解析槽位=仅话术；上下文=界面/会话态（路由：解析槽位 > 上下文）。"""
    ctx_parts: list[str] = []
    parsed_parts: list[str] = []
    hint = CARD_CONTEXT_HINT.get(cur)
    if hint:
        ctx_parts.append(hint)

    raw = (raw_slots or "").strip()
    if raw:
        for seg in re.split(r"[；;]", raw):
            s = seg.strip()
            if not s:
                continue
            if "（来自上文）" in s:
                base = s.replace("（来自上文）", "").strip()
                if base:
                    parsed_parts.append(base)
                ctx_parts.append("保留上文功能意图")
                continue
            if s.startswith("缺："):
                parsed_parts.append(s)
            elif _is_context_segment(s):
                ctx_parts.append(s)
            else:
                parsed_parts.append(s)

    parsed = "；".join(parsed_parts) if parsed_parts else "—"
    context = "；".join(ctx_parts) if ctx_parts else "—"
    return parsed, context


def row_section_id(row: tuple[str, ...]) -> str:
    """Markdown 章节键（与 group_rows 的 fn 不同：全局拆为 global_cap / init）。"""
    cur, fn = row[0], row[1]
    if fn == "全局" and cur == "任意业务卡":
        return "global_cap"
    if fn == "全局":
        return "init"
    return fn


def to_export_row(row: tuple[str, ...]) -> tuple[str, str, str, str, str]:
    cur, _fn, _intent, mode, utter, tgt, slots = row
    parsed, context = split_parsed_and_context(cur, slots)
    return (context, utter, mode, parsed, tgt)


