def route_dedupe_key(row: tuple[str, ...]) -> tuple[str, str, str, str, str]:
    """去重键：功能章节 + 交互模式 + 解析槽位 + 上下文 + 目标卡片（不含用户输入、不含当前卡片）。"""
    context, _utter, mode, parsed, tgt = to_export_row(row)
    return (row_section_id(row), mode, parsed, context, tgt)


def dedupe_output_rows(rows: list[tuple[str, ...]]) -> list[tuple[str, ...]]:
    """同一路由（五列）只保留首条用户输入示例。"""
    seen: set[tuple[str, str, str, str, str]] = set()
    out: list[tuple[str, ...]] = []
    for r in rows:
        key = route_dedupe_key(r)
        if key in seen:
            continue
        seen.add(key)
        out.append(r)
    return out


def find_utterance_slot_conflicts(rows: list[tuple[str, ...]]) -> list[str]:
    """同功能章节 + 用户输入 + 交互模式 下，解析槽位不一致则报出。"""
    buckets: dict[tuple[str, str, str], set[str]] = {}
    for r in rows:
        sec = row_section_id(r)
        _ctx, utter, mode, parsed, _tgt = to_export_row(r)
        key = (sec, utter, mode)
        buckets.setdefault(key, set()).add(parsed)
    conflicts: list[str] = []
    sec_titles = {
        "global_cap": "全局能力",
        "init": "初始化页面",
        "待跟进": "待跟进",
        "方案速配": "方案速配",
        "产品报价": "方案报价",
        "确认下单": "确认订单",
    }
    for (sec, utter, mode), parsed_set in buckets.items():
        if len(parsed_set) <= 1:
            continue
        title = sec_titles.get(sec, sec)
        conflicts.append(
            f"{title} | {utter} | {mode} → {len(parsed_set)} 种解析槽位: "
            + " || ".join(sorted(parsed_set))
        )
    return conflicts


def rows_for_section(rows: list[tuple[str, ...]], section_id: str) -> list[tuple[str, ...]]:
    return [r for r in rows if row_section_id(r) == section_id]


def target_order_index(section_id: str, tgt: str) -> int:
    order = SECTION_TARGET_ORDER.get(section_id, [])
    if tgt in order:
        return order.index(tgt)
    for i, name in enumerate(order):
        if name in tgt or tgt in name:
            return i
    return len(order) + 100


def sort_section_rows(rows: list[tuple[str, ...]]) -> list[tuple[str, ...]]:
    """章内：目标卡片主流程顺序 → 同卡内按交互模式 → 用户输入。"""
    return sorted(
        rows,
        key=lambda r: (
            target_order_index(row_section_id(r), r[5]),
            MODE_ORDER.get(r[3], 9),
            r[4],
        ),
    )


def sort_all_export_rows(rows: list[tuple[str, ...]]) -> list[tuple[str, ...]]:
    buckets: dict[str, list[tuple[str, ...]]] = {s: [] for s in SECTION_EXPORT_ORDER}
    for r in rows:
        sid = row_section_id(r)
        buckets.setdefault(sid, []).append(r)
    out: list[tuple[str, ...]] = []
    for sid in SECTION_EXPORT_ORDER:
        out.extend(sort_section_rows(buckets.get(sid, [])))
    for sid, part in buckets.items():
        if sid not in SECTION_EXPORT_ORDER:
            out.extend(sort_section_rows(part))
    return out


