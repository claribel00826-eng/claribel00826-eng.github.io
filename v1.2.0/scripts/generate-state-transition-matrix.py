def _append_section_table(
    lines: list[str],
    header_row: str,
    sep_row: str,
    section_rows: list[tuple[str, ...]],
) -> None:
    if not section_rows:
        return
    lines.append(header_row)
    lines.append(sep_row)
    for r in sort_section_rows(section_rows):
        cells = [c.replace("|", "\\|") for c in to_export_row(r)]
        lines.append("| " + " | ".join(cells) + " |")
    lines.append("")


